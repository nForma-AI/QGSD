#!/usr/bin/env node
'use strict';
// bin/adapters/emitter-tla.test.cjs
// Tests for TLA+ emitter.

const { test } = require('node:test');
const assert = require('node:assert');
const { emitTLA } = require('./emitter-tla.cjs');

function minimalIR() {
  return {
    machineId: 'test-fsm',
    initial: 'idle',
    stateNames: ['idle', 'running', 'done'],
    finalStates: ['done'],
    transitions: [
      { fromState: 'idle', event: 'START', guard: null, target: 'running', assignedKeys: [] },
      { fromState: 'running', event: 'FINISH', guard: 'isReady', target: 'done', assignedKeys: ['count'] },
    ],
    ctxVars: ['count'],
    ctxDefaults: { count: 0 },
    sourceFile: 'test.ts',
    framework: 'xstate-v5',
  };
}

test('emitTLA produces TLA+ content with MODULE header', () => {
  const { tlaContent } = emitTLA(minimalIR(), { dry: true, moduleName: 'TestFsm' });
  assert.ok(tlaContent.includes('---- MODULE TestFsm_xstate ----'));
});

test('emitTLA produces VARIABLES section with state and ctxVar', () => {
  const { tlaContent } = emitTLA(minimalIR(), { dry: true });
  assert.ok(tlaContent.includes('VARIABLES'));
  assert.ok(tlaContent.includes('state'));
  assert.ok(tlaContent.includes('count'));
});

test('emitTLA produces Init with correct initial state', () => {
  const { tlaContent } = emitTLA(minimalIR(), { dry: true });
  assert.ok(tlaContent.includes('state = "idle"'));
});

test('emitTLA produces cfg with SPECIFICATION Spec', () => {
  const { cfgContent } = emitTLA(minimalIR(), { dry: true });
  assert.ok(cfgContent.includes('SPECIFICATION Spec'));
});

test('emitTLA header says GENERATED by bin/fsm-to-tla.cjs', () => {
  const { tlaContent } = emitTLA(minimalIR(), { dry: true });
  assert.ok(tlaContent.includes('GENERATED by bin/fsm-to-tla.cjs'));
});

test('emitTLA throws on invalid IR', () => {
  assert.throws(() => {
    emitTLA({ machineId: '', stateNames: [] }, { dry: true });
  }, /Invalid MachineIR/);
});

test('emitTLA includes guard FIXME for unmapped guard', () => {
  const { tlaContent } = emitTLA(minimalIR(), { dry: true });
  assert.ok(tlaContent.includes('FIXME: guard isReady'));
});

test('emitTLA uses guard expression when provided in userGuards', () => {
  const { tlaContent } = emitTLA(minimalIR(), {
    dry: true,
    userGuards: { isReady: 'count > 0' },
  });
  assert.ok(tlaContent.includes('count > 0'));
});
